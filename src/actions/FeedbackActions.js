export function setEmail(value) {
    return {
        type: 'SET_EMAIL',
        payload: value,
    }
}

export function setName(value) {
    return {
        type: 'SET_NAME',
        payload: value,
    }
}

export function setText(value) {
    return {
        type: 'SET_TEXT',
        payload: value,
    }
}
